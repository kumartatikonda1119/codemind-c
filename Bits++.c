#include<stdio.h>
int main()
{
    int x,c=0;
    scanf("%d",&x);
    char a[1000];
   
    for(int i=1;i<=x;i++){
         scanf("%s",&a);
    
    {
        if(a[1]=='+')
        {
            c++;
            continue;
            
            
        }else {
            c--;
            continue;
            
            
        }
    }
    }
    printf("%d",c);
    
    
}